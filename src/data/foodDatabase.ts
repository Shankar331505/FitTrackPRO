// Helper function to search foods
export const searchFoods = (query: string): Food[] => {
    const lowerQuery = query.toLowerCase();
    return foodDatabase.filter(food =>
        food.name.toLowerCase().includes(lowerQuery) ||
        food.category.toLowerCase().includes(lowerQuery)
    );
};

// Helper function to get foods by category
export const getFoodsByCategory = (category: string): Food[] => {
    return foodDatabase.filter(food => food.category === category);
};

// Get all unique categories
export const getCategories = (): string[] => {
    return Array.from(new Set(foodDatabase.map(food => food.category)));
};
